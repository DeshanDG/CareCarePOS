    public boolean addEmployee(){
    boolean isAdded = false;
    First_name = txt_firstname.getText();
    Last_name = txt_lastname.getText();
    City = txt_city.getText();
    HiredDate = txt_hiredDate.getText();
    Email = txt_Email.getText();
    Sontact = txt_contactNum.getText();
    Catogary = (String) Categorycombo.getSelectedItem();
    try{
    Connection con = DbConnection.getConnection();
    String query = "INSERT INTO employees( First_name, Last_name, City ,HiredDate, Email,ContactNumber , Category ) VALUES (?,?,?,?,?,?,?)";
    PreparedStatement pst = con.prepareStatement(query);
    
    pst.setString(1, First_name);
    pst.setString(2, Last_name);
    pst.setString(3, City);
    pst.setString(4, HiredDate);
    pst.setString(5, Email);
    pst.setString(6, Sontact);
    pst.setString(7, Catogary);
    
    int rowCount = pst.executeUpdate();
    
    if(rowCount>0){
        isAdded = true;
    }
    else{
    isAdded = false;
    }
    }
    
    catch(Exception e){
    e.printStackTrace();
    }
    return isAdded;
    }
    

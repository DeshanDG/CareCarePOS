    public void setRecordstoEmployees(){
    try{
     Class.forName("com.mysql.cj.jdbc.Driver");
     Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/carecaredb","root",""); 
     Statement st = con.createStatement();
     ResultSet rs = st.executeQuery("SELECT * FROM employees ");
     
     while(rs.next()){
     String EmployeeiD = rs.getString("EmployeeId");
     String Firstname = rs.getString("First_name");
     String Lastname = rs.getString("Last_Name");
     String city = rs.getString("City");
     String EMail = rs.getString("Email");
     String HireDate = rs.getString("hiredDate");
     String Cnumber = rs.getString("ContactNumber");
     String Cat = rs.getString("EmpStatus");
     
     Object[] obj = {EmployeeiD,Firstname,Lastname, EMail ,city , HireDate , Cnumber, Cat} ;
     model = (DefaultTableModel) Employee_tbl.getModel();
     model.addRow(obj);    
     }   
    }
    
    catch(Exception e){
        e.printStackTrace();
    }
    }
    
